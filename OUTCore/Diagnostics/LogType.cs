namespace OUTCore.Diagnostics
{
    /// <summary>
    /// The types of messages that can be logged.
    /// </summary>
    public enum LogType
    {
        /// <summary>
        /// Indicates that a debug message is being logged.
        /// </summary>
        Debug,
        /// <summary>
        /// Indicates that an info message is being logged.
        /// </summary>
        Info,
        /// <summary>
        /// Indicates that a warning message is being logged.
        /// </summary>
        Warning,
        /// <summary>
        /// Indicates that an error message is being logged.
        /// </summary>
        Error,
        /// <summary>
        /// Indicates that a fatal message is being logged.
        /// </summary>
        Fatal,
    }
}





